package JavaBasico;

public class Varaiables {
    public static void main(String[] args) {

        // enteros
        byte number1 = 1; // 1 byte
        short number2 = 2; // 2 bytes
        int number3 = 3; // 4 bytes
        long number4 = 4; // 8 bytes

        // punto flotante
        float decimal1 = 4.9f;
        double decimal2 = 9.99d;

        //caracter
        char caracter1 = 'a';

        //booleanos
        boolean verdadero = true;
        boolean falso = false;

        //cadena de texto
        String nombre = "Juan Domingo";
        String apellido = "Peron";

        // tipos envoltorio
        Integer numero1 = null;
        Long numero2 = 2L;


    }
}
